
import UIKit

extension UIColor {
    @nonobjc class var tomato: UIColor {
        return UIColor(red: 224.0 / 255.0, green: 66.0 / 255.0, blue: 32.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var coolGrey: UIColor {
        return UIColor(red: 167.0 / 255.0, green: 169.0 / 255.0, blue: 172.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var topaz: UIColor {
        return UIColor(red: 23.0 / 255.0, green: 190.0 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var dark: UIColor {
        return UIColor(red: 35.0 / 255.0, green: 42.0 / 255.0, blue: 56.0 / 255.0, alpha: 1.0)
    }
}
